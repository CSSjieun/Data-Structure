package org.CSSjieun.eclipse.Operator;

public class ArthmeticDemo {

	public static void main(String[] args) {
		// result의 값은 3
		int result = 1+ 2;
		System.out.println(result);
		
		// result의 값은 2
		result = result - 1;
		System.out.println(result);
		
		// result의 값은 4
		result = result * 2;
		System.out.println(result);
		
		// result의 값은 2
		result = result / 2;
		System.out.println(result);
		
		// result의 값은 10
		result = result + 8;
		// 나머지 3
		result = result % 7;
		System.out.println(result);
				

	}

}
